#This module provides access to the mathematical functions defined by the C standard.
import math

#constants
print(math.pi)
print(math.e)

#converting radians to degree
print(math.degrees(0.1))

#printing reverse of  cos, sin , tan value
print(math.asin(0.5))
print(math.acos(0.5))
print(math.atan(0.5))

#printing sin, tan, cos values
print(math.sin(3))
print(math.cos(1))
print(math.tan(2))

# e^x
print(math.exp(2))

#log(x, base)
print(math.log(10,10))
